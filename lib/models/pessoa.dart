class Pessoa {
  String _nome = "";
  double _peso = 0.0;
  double altura = 0.0;

  void setNome(String nome) => _nome = nome;
  String getNome() => _nome;

  void setPeso(double peso) => _peso = peso;
  double getPeso() => _peso;

  void setAltura(double altura) => altura = altura;
  double getAltura() => altura;
}
